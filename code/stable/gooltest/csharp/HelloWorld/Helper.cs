public class Helper {
    
    /** \brief This function adds two numbers
        \param num1 First number to add
        \param num2 Second number to add
        \return Sum
    */
    public static double doubleAndAdd(double num1, double num2) {
        double doubledSum;
        doubledSum = 2.0 * num1 + 2.0 * num2;
        return doubledSum;
    }
}
