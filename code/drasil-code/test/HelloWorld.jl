module HelloWorld

# Get input
filePath = "input.txt"
open(filePath) do fileHandle
    global fileContents = readlines(fileHandle)
end

# Process input
splitLines = [split(line, "   ") for line in fileContents]
list1 = sort([parse(Int64, line[1]) for line in splitLines])
list2 = sort([parse(Int64, line[2]) for line in splitLines])

# Calculate distance and print out
distance = 0
for (l1, l2) in zip(list1, list2)
    global distance += abs(l1 - l2)
end
println("Distance: $distance")

end