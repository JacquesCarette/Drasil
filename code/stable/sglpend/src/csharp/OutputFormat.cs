using System;
using System.IO;

public class OutputFormat {
    
    /** \brief Writes the output values to output.txt
        \param θ_p displacement angle of the pendulum (rad)
    */
    public static void write_output(double θ_p) {
        StreamWriter outputfile;
        outputfile = new StreamWriter("output.txt", false);
        outputfile.Write("θ_p = ");
        outputfile.WriteLine(θ_p);
        outputfile.Close();
    }
}
