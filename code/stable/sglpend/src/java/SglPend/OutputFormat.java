import java.io.File;
import java.io.FileWriter;
import java.io.IOException;
import java.io.PrintWriter;

public class OutputFormat {
    
    /** \brief Writes the output values to output.txt
        \param θ_p displacement angle of the pendulum (rad)
    */
    public static void write_output(double θ_p) throws IOException {
        PrintWriter outputfile;
        outputfile = new PrintWriter(new FileWriter(new File("output.txt"), false));
        outputfile.print("θ_p = ");
        outputfile.println(θ_p);
        outputfile.close();
    }
}
